class Account {
    constructor(username, password) {
      this.username = username;
      this.password = password;
    }
  
    // Getter for username
    getUsername() {
      return this.username;
    }
  
    // Setter for username
    setUsername(username) {
      this.username = username;
    }
  
    // Getter for password
    getPassword() {
      return this.password;
    }
  
    // Setter for password
    setPassword(password) {
      this.password = password;
    }
  }